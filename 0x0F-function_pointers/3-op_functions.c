/**
 * op_add - add two numbers
 * @a: number to use
 * @b: numbers to use
 * Return: none
 *
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - minus numbers
 * @a: number to use
 * @b: number to use
 * Return: result got
 */

int op_sub(int a, int b)
{
	return (a - b);
}
